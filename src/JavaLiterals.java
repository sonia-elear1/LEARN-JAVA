public class JavaLiterals {
    public static void main(String[] args) {
        byte age = 110; // type is byte so value is always between -127 to 128
        int age1 = 217987;
        float val = 1.33F; // as default is double so add 'f' or 'F' at last
        double val1 = 1.34;
        char ch  = 'S'; // it will contain string with only one character
        String ch1 = "sona"; // want to add multiple char add as String
        long value = 34567899857L; // contains L at last for long
        boolean isTrue = true;
        System.out.println("literals explained for: -\nbyte,int,float,double,char,long, String and boolean data types ");
    }
}
